	<div class="moduleIndent">
		<p class="text-fugefa">
			<em>„Okuljatok a fügefa példáján: amikor már zsendül az ága, és levelet hajt, tudjátok, hogy közel van a nyár. Így ti is, amikor mindezt látjátok, vegyétek észre, hogy közel van ő, az ajtó előtt.” (Mt 24,32-33)</em>
		</p>
		<p>A Fügefa modul közösségei vállalják, hogy együttműködnek a régió közösségeivel, közös alkalmakat szerveznek, akár más modulok közösségeinek bevonásával. Teremtésvédelmi programjukban szempont, hogy a közösség életének minél több szegmensében megjelenjen a teremtésvédelmi munka.</p>
