		<p class="text-liliom"><em>„Figyeljétek meg a mező liliomait, hogyan növekednek: nem fáradoznak és nem fonnak, de mondom nektek, hogy Salamon teljes dicsőségében sem öltözött úgy, mint ezek közül akár csak egy is.” (Mt 6,28-29)</em></p>
		<p>A Liliom modulban a közösség saját, egyedi teremtésvédelmi programtervét állítja össze, megfogalmaz magának lehetőségeihez mért célokat. A közösségében megvalósuló teremtésvédelmi programokról hírt ad, meghívást küld a környezetében lévő közösségeknek.</p>
	</div>

	<img src="/assets/img/stat/2m_3szolovesszo.jpg" alt="Szőlővessző" />
	<h4 class="text-szolovesszo">Szőlővessző</h4>
	<div class="moduleIndent">
		<p class="text-szolovesszo">
			<em>„Én vagyok a szőlőtő, ti a szőlővesszők: aki énbennem marad, és én őbenne, az terem sok gyümölcsöt, mert nélkülem nem tudtok semmit cselekedni. (…) Ha megmaradtok énbennem, és beszédeim megmaradnak tibennetek, akkor bármit akartok, kérjétek, és megadatik nektek.” (Jn 15,5.7)</em>
		</p>
		<p>A Szőlővessző modulban a közösségek vállalják, hogy segítenek, ha a programban résztvevő közösségek kérdéseikkel, tanácsért hozzá fordulnak. Teremtésvédelmi programjaikat igyekeznek többféle vállalási terület felé bővíteni.</p>
	</div>

