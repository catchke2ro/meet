	<h1>A modulok</h1>
	<p>A közösségi szerepvállalás, a teremtést óvó vállalások és elköteleződés mértékétől függően, a közösségek öt különböző modulból választhatják részvételüket a programban. A közösségek a kiválasztott modulnak megfelelő programterv összeállítását követően egy-egy modul arculati elemeit maguk is jogosultak használni. (Feltűntethetik elektronikus kommunikációs felületeiken, levelezésben, hirdetményeikben.)</p>
	<p>A modulok szimbólumai bibliai növények, melyek mindegyike szép, a maga nemében mindegyik teljes, és fontos üzenetet hordoz. A modulok kereteket adnak, mely kereteken belül megfogalmazott teremtésvédelmi program a közösség egyedi vállalása. Az egyes modulokhoz tartozás elsősorban a közösségi szerepvállalás és elköteleződés mértékének függvénye.</p>

	<img src="/assets/img/stat/2m_1mustar.jpg" alt="Mustármag" />
	<h4 class="text-mustarmag">Mustármag</h4>
	<div class="moduleIndent">
		<p class="text-mustarmag">
			<em>„Hasonló a mennyek országa a mustármaghoz, amelyet fog az ember, és elvet szántóföldjébe. Ez kisebb ugyan minden magnál, de amikor felnő, nagyobb minden veteménynél, és fává lesz, úgyhogy eljönnek az égi madarak és fészket raknak ágai között.” (Mt 13,31-32)</em>
		</p>
		<p>A Mustármag a belépő modul a programban, minden csatlakozó közösség induló mustármag. Ebben a modulban, csatlakozáskor minden közösség vállalja, hogy:</p>
		<ul>
			<li>istentiszteletein rendszeresen (legalább havonta egyszer) imádkozik a teremtett világért,</li>
			<li>minden évben megünnepli a teremtés hetét,</li>
			<li>megosztja (legalább évente egyszer) másokkal a közösségben megvalósuló jógyakorlatait.</li>
		</ul>
		<p>Valamint a közösség vállalja, hogy a MEET programot saját felületein és csatornáin hirdeti más közösségek számára is.</p>
	</div>

	<img src="/assets/img/stat/2m_2liliom.jpg" alt="Mező liloma" />
	<h4 class="text-liliom">Mező lilioma</h4>
