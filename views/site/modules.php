		</p>
		<p>A Cédrus modulban a résztvevő közösségek vállalják, hogy a régiójukban folyó teremtésvédelmi munkát aktívan koordinálják, a környezetükben lévő közösségeket megkeresik és támogatják őket teremtésvédelmi céljaik megvalósításában. A modul közösségei maguk is minél széleskörűbb teremtésvédelmi programot valósítanak meg, és szerzett tapasztalataikat megosztják a többiekkel.</p>
